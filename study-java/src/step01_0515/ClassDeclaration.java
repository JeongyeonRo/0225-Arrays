package step01_0515;
//클래스 선언하기
public class ClassDeclaration {
	//메인메소드 선연
	public static void main(String[] args) {
		//콘솔에 출력
		//Java에서 출력하는 방법
		//print(): 괄호 안의 내용을 출력
		//println(): 괄호 안의 내용을 출력하고 줄바꿈a
		//printf(): 서식을 지정해서 괄호 안의 내용을 출력
		System.out.print("첫번째 자바 프로그램");
		// \t: 탭 간격
		System.out.print("안녕하세요\t좋은아침입니다.");
		// \n: 줄바꿈
		System.out.println("안녕하세요\nHello");
		// 연결 +
		System.out.print("3+4="+(3+4));
		
	}
}
